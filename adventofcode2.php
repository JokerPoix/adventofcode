abstract class Sign {
    public const Rock = 'A';
    public const Paper = 'B';
    public const Scissor = 'C';
}

function isWin($mySign, $oppenentSign) {
	$VICTORY_CONDITION = [
		"A" => "C",
		"B" => "A",
		"C" => "B"
	];
	
	if ($mySign === $oppenentSign) {
		return null;
	}
	
	return $VICTORY_CONDITION[$mySign] === $oppenentSign;
};

function matchSign($uncommonSsign) {
	switch ($uncommonSsign) {
		case 'X':
			return Sign::Rock;
		case 'Y':
			return Sign::Paper;
		case 'Z':
			return Sign::Scissor;
			
		default:
			return null;
	}
}

function getMatchScore($win) {
	if ($win === null) {
		return 3;
	}
	
	return $win ? 6 : 0;
}

function getBaseScore($sign) {
	switch($sign) {
		case Sign::Rock:
			return 1;
		case Sign::Paper:
			return 2;
		case Sign::Scissor:
			return 3;
			
		default:
			return 0;
	}
}

function run($matches) {
	function sum($carry, $match) {
		$explodedMatch = explode(' ', $match);
		$mySign = matchSign($explodedMatch[1]);
		$opponentSign = $explodedMatch[0];
		
		$win = isWin($mySign, $opponentSign);
		
		$baseScore = getBaseScore($mySign);
		$matchScore = getMatchScore($win);
		
		$carry += $baseScore + $matchScore;
		
	    return $carry;
	}
	
	return array_reduce($matches, "sum");
}

